public class Operators 
{
    public static void main(String[] args) {

        //additon of 2 numbers
        int a = 10;
        int b = 17;
        int result0 = a + b;
        System.out.println("Add of "+ a+ " and " + b+ " : " +result0);

        //subraction of 2 numbers
        int c = 10;
        int d = 17;
        int result1 = c - d;
        System.out.println("Sub of "+ c+ " and " + d+ " : " +result1);

        //multiplication of 2 numbers
        int i = 10;
        int j = 17;
        int result2 = i * j;
        System.out.println("Multiplication of "+ i+ " and " + j+ " : " +result2);

        //division(quotient) of 2 numbers
        int x = 100;
        int y = 17;
        int result3 = x / y;
        System.out.println("Division of "+ x+ " and " + y+ " : " +result3);

        // modules(reminder) of 2 numbers
        int p = 10;
        int q = 17;
        int result4 = p % q;
        System.out.println("Mod of "+ p+ " and " + q+ " : " +result4);


    }
}
