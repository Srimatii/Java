public class Forloop {
    public static void main(String[] args) {
        for(int i = 1;i <=4 ; i++)  //increment
        {
            System.out.println("Hello World " +i);
        }
        System.out.println("");

        for(int i = 4;i >= 1 ; i--)  //decrement
        {
            System.out.println("Hello World " +i);
        }
    }
}
